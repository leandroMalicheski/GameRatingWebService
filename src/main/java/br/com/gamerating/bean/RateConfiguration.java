package br.com.gamerating.bean;

public class RateConfiguration {

	int jogability;
	int fun;
	int sound;
	int immersion;
	String userLogin;
	
	public int getJogability() {
		return jogability;
	}
	public void setJogability(int jogability) {
		this.jogability = jogability;
	}
	public int getFun() {
		return fun;
	}
	public void setFun(int fun) {
		this.fun = fun;
	}
	public int getSound() {
		return sound;
	}
	public void setSound(int sound) {
		this.sound = sound;
	}
	public int getImmersion() {
		return immersion;
	}
	public void setImmersion(int immersion) {
		this.immersion = immersion;
	}
	public String getUserLogin() {
		return userLogin;
	}
	public void setUserLogin(String userLogin) {
		this.userLogin = userLogin;
	}
	
	
}
